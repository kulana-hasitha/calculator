<?php

class Cal{
    public function add($first_num, $second_num){
        return  $result = $first_num + $second_num;
    }

    public function sub($first_num, $second_num){
        return  $result = $first_num - $second_num;
    }

    public function mul($first_num, $second_num){
        return  $result = $first_num * $second_num;
    }

    public function div($first_num, $second_num){
        return  $result = $first_num / $second_num;
    }

    public function mod($first_num, $second_num){
        return  $result = $first_num % $second_num;
    }
}
